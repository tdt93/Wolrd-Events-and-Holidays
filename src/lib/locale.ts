export function t(key: string, lang: AppLanguage): string {
  return UI[lang]?.[key] ?? UI.en[key] ?? key;
}

export function tf(
  key: string,
  lang: AppLanguage,
  vars: Record<string, string | number>,
): string {
  let text = t(key, lang);
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(`{${k}}`, String(v));
  }
  return text;
}

export function holidayTypeLabel(type: HolidayType, lang: AppLanguage): string {
  return t(`holiday_${type}`, lang);
}

export function eventCategoryLabel(
  category: EventCategory,
  lang: AppLanguage,
): string {
  return t(`event_${category}`, lang);
}

export function celebrationsLabel(count: number, lang: AppLanguage): string {
  if (count === 1) return t("celebrationCount", lang);
  return tf("celebrationsCount", lang, { count });
}

export function countryNameInLang(
  iso2: string,
  lang: AppLanguage,
): string | undefined {
  ensureLocales();
  const code = iso2.toUpperCase();
  return (
    countries.getName(code, lang) ??
    countries.getName(code, "en") ??
    undefined
  );
}

export function localizeCountryList(
  list: { countryCode: string; name: string }[],
  lang: AppLanguage,
) {
  return list.map((c) => ({
    ...c,
    name: countryNameInLang(c.countryCode, lang) ?? c.name,
  }));
}
